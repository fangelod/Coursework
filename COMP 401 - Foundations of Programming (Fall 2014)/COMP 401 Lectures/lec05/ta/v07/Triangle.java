package ta.v07;

public class Triangle {

	double ax;
	double ay;
	double bx;
	double by;
	double cx;
	double cy;
	
	Triangle(double x1, double y1,
			 double x2, double y2,
			 double x3, double y3) {
		this.ax = x1;
		this.ay = y1;
		this.bx = x2;
		this.by = y2;
		this.cx = x3;
		this.cy = y3;
	}
}
