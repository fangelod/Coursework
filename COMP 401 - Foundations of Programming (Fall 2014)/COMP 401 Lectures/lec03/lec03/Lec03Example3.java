package lec03;

public class Lec03Example3 {

	public static void main(String[] args) {
		
		// Create a string from a literal
		String s1 = "This is a string";
		
		// Create a string using "new"
		// String s2 = "Another string";
		String s2 = new String("Another string");

		// From the result of the + operator
		
		String s3 = "This" + " plus " + "that";
		
		// Value data types automatically converted
		// to strings if concatenated to a string using +
		
		int a = 4;
		double b = 3.24;
		String s4 = "a = " + a + ", b = " + b;
		
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
		System.out.println(s4);
		
	}

}
